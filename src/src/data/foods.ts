/**
 * 根据 ID 获取食物
 */
export function getFoodById(id: string): FoodItem | undefined {
  return builtinFoods.find(food => food.id === id)
}

/**
 * 根据名称搜索食物（支持别名）
 */
export function searchFood(query: string): FoodItem[] {
  const lowerQuery = query.toLowerCase()
  return builtinFoods.filter(food =>
    food.name.toLowerCase().includes(lowerQuery) ||
    food.aliases.some(alias => alias.toLowerCase().includes(lowerQuery))
  )
}

/**
 * 根据分类获取食物
 */
export function getFoodsByCategory(category: string): FoodItem[] {
  return builtinFoods.filter(food => food.category === category)
}